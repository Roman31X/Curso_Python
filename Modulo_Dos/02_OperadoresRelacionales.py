"""
||  OPERADOR    ||      COMPARACION     ||
||==============||======================||
||      ==      ||       IGUAL QUE      ||
||      !=      ||     DISTINTO QUE     ||
||      <       ||       MENOR QUE      ||
||      <=      ||   MENOR O IGUAL QUE  ||
||      >       ||       MAYOR QUE      ||
||      >=      ||   MAYOR O IGUAL QUE  ||
"""
#PRUEBA DE OPERADORES RELACIONALES TODOS DAN TRUE O VERDAD

r = 10 == 10
print(r)
print('======')

r = 5 != 10
print(r)
print('======')

r = 5 < 10
print(r)
print('======')

r = 10 <= 10
print(r)
print('======')

r = 10 > 5
print(r)
print('======')

r = 10 >= 10
print(r)
print('======')
